"""
题目：古典问题：有一对兔子，从出生后第3个月起每个月都生一对兔子，小兔子长到第三个月后每个月又生一对兔子，假如兔子都不死，问每个月的兔子总数为多少？
程序分析：兔子的规律为数列1,1,2,3,5,8,13,21....
"""

x = int(raw_input("请输入你要查看的月份：\n"))
f1 = 1
f2 = 1
n = 1
if(x == 1 or x == 2):
  print ("第%d月有%d对" % (x,n)) 
elif (x > 2):
    for i in range(2,x):
       n = f1 + f2
       f1 = f2
       f2 = n
    print ("第%d月有%d对" % (x,n))
